const TRAILING = /^(.*?)([\s()[\]{}.,:;!?'"“”‘’\-–—←→✓✅❌➕📎📋📍📞📢📣📤📥📦📧📭📱📸🔄🔍🔒🔢🗑️🚀🚪🤝⚙️⚠️⛺🏕️🏛️🏠👷💾📊📝📰🎥🏆🆘🇮🇳🌐🎉▶●]+)$/u;

function translateCore(value) {
  if (TA[value]) return TA[value];

  const withoutRequired = value.replace(/\s*\*$/, '');
  if (withoutRequired !== value && TA[withoutRequired]) return `${TA[withoutRequired]} *`;

  const withoutColon = value.replace(/:$/, '');
  if (withoutColon !== value && TA[withoutColon]) return `${TA[withoutColon]}:`;

  const withoutArrow = value.replace(/\s*[→›]$/, '');
  if (withoutArrow !== value && TA[withoutArrow]) return `${TA[withoutArrow]} →`;

  const withoutEmoji = value.replace(/^[^\p{L}\p{N}]+/u, '').trim();
  if (withoutEmoji !== value && TA[withoutEmoji]) return value.replace(withoutEmoji, TA[withoutEmoji]);

  return value;
}

export function literalT(value) {
  if (i18n.language !== 'ta' || typeof value !== 'string') return value;
  if (!/[A-Za-z]/.test(value)) return value;

  const leading = value.match(LEADING);
  if (leading) {
    const translated = translateCore(leading[2]);
    if (translated !== leading[2]) return `${leading[1]}${translated}`;
  }

  const trailing = value.match(TRAILING);
  if (trailing) {
    const translated = translateCore(trailing[1]);
    if (translated !== trailing[1]) return `${translated}${trailing[2]}`;
  }

  return translateCore(value);
}

export function complaintCategoryT(category, customCategory) {
  if (category === 'Others' && customCategory) return customCategory;
  return literalT(category || 'Others');
}
